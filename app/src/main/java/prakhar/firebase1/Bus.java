public class Bus {
    private int bus_number;
    private String start;
    private String end;
    private String via;
    private String time;

    public Bus() {
    }
    public int getBusNumber(){
        return bus_number;
    }
    public String getStart() {
        return start;
    }
    public String getEnd() {
        return end;
    }
    public String getVia() {
        return via;
    }
    public String getTime() {
        return time;
    }
}
