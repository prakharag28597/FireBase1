public class Bus {
    private int busnumber;
    private String start;
    private String end;
    private String via1;
    private String via2;
    private String time;

    public Bus() {
    }
    public int getBusnumber(){
        return busnumber;
    }
    public String getStart() {
        return start;
    }
    public String getEnd() {
        return end;
    }
    public String getVia1() {
        return via1;
    }
    public String getVia2() {
        return via2;
    }
    public String getTime() {
        return time;
    }
}
